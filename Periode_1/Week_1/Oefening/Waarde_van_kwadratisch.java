public class Waarde_van_kwadratisch {
    public static void main(String []args){
        double x = 0;
        double value = (3*(x*x))-(8*x)+4;
        System.out.println("Voor X = " +x+ " is de waarde " + value);

        x=2;
        value = (3*(x*x))-(8*x)+4;
        System.out.println("Voor X = " +x+ " is de waarde " + value);

        x=4;
        value = (3*(x*x))-(8*x)+4;
        System.out.println("Voor X = " +x+ " is de waarde " + value);
    }
}
